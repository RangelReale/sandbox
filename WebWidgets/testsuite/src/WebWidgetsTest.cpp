#include "Poco/SAX/InputSource.h"
#include "Poco/WebWidgets/HTMLVisitor.h"
#include "Poco/WebWidgets/Parser.h"
#include <sstream>


using namespace Poco::WebWidgets;


WebWidgetsTest::WebWidgetsTest(const std::string& name): CppUnit::TestCase(name)
{
}


WebWidgetsTest::~WebWidgetsTest()
{
}


void WebWidgetsTest::doTest(std::istream& istr, std::ostream& ostr)
{
	Poco::XML::InputSource src(istr);
	Poco::XML::DOMParser parser;
	Poco::AutoPtr<Poco::XML::Document> pDoc = parser.parse(&src);
	Poco::XML::Element* pRoot = pDoc->documentElement();
	Poco::AutoPtr<Widget> ptrRoot = Parser::instance().load(pRoot);
	HTMLVisitor ht(ostr, Poco::URI());
	ptrRoot->accept(ht);
}


void WebWidgetsTest::setUp()
{
}


void WebWidgetsTest::tearDown()
{
}


CppUnit::Test* WebWidgetsTest::suite()
{
	CppUnit::TestSuite* pSuite = new CppUnit::TestSuite("WebWidgetsTest");

	CppUnit_addDataTest(pSuite, WebWidgetsTest, doTest, "tests/", "temp/");

	return pSuite;
}
