#include "AsyncChannelTest.h"
#include "CppUnit/TestCaller.h"
#include "CppUnit/TestSuite.h"
#include "Poco/DeviceIO/AsyncStreamChannel.h"
#include "Poco/DeviceIO/AsyncCommand.h"
#include "Poco/DeviceIO/AsyncEvent.h"
#include "Poco/Delegate.h"
#include <sstream>


using Poco::DeviceIO::AsyncStreamChannel;
using Poco::DeviceIO::AsyncCommand;
using Poco::DeviceIO::AsyncReadCommand;
using Poco::DeviceIO::AsyncWriteCommand;
using Poco::DeviceIO::AsyncSeekCommand;
using Poco::DeviceIO::AsyncEvent;
using Poco::ActiveResult;
using Poco::delegate;


AsyncChannelTest::AsyncChannelTest(const std::string& name): 
	CppUnit::TestCase(name),
	_completed(0),
	_failed(0)
{
}


AsyncChannelTest::~AsyncChannelTest()
{
}


void AsyncChannelTest::testWrite()
{
	std::stringstream str;
	AsyncStreamChannel channel(str);
	channel.enqueue(new AsyncWriteCommand("Hello", 5));
	channel.enqueue(new AsyncWriteCommand(", ", 2));
	ActiveResult<int> result = channel.enqueue(new AsyncWriteCommand("world!", 6));
	result.wait();
	std::string s(str.str());
	assert (s == "Hello, world!");
}


void AsyncChannelTest::testRead()
{
	std::istringstream istr("Hello, world!");
	char buffer[16];
	AsyncStreamChannel channel(istr);
	ActiveResult<int> result = channel.enqueue(new AsyncReadCommand(buffer, sizeof(buffer)));
	result.wait();
	std::string s(buffer, result.data());
	assert (s == "Hello, world!");
}


void AsyncChannelTest::testSeek()
{
	std::istringstream istr("Hello, world!");
	char buffer[16];
	AsyncStreamChannel channel(istr);
	channel.enqueue(new AsyncSeekCommand(7, std::ios::beg));
	ActiveResult<int> result = channel.enqueue(new AsyncReadCommand(buffer, 5));
	result.wait();
	std::string s(buffer, result.data());
	assert (s == "world");
}


void AsyncChannelTest::testEvents()
{
	std::stringstream str;
	AsyncStreamChannel channel(str);
	AsyncCommand::Ptr pWrite(new AsyncWriteCommand("Hello, world!", 13));
	pWrite->commandCompleted += delegate(this, &AsyncChannelTest::onCompleted);
	channel.commandCompleted += delegate(this, &AsyncChannelTest::onCompleted);
	assert (pWrite->state() == AsyncCommand::CMD_PENDING);
	ActiveResult<int> result = channel.enqueue(pWrite);
	pWrite->wait();
	result.wait();
	assert (pWrite->succeeded());
	assert (!pWrite->failed());
	assert (pWrite->state() == AsyncCommand::CMD_COMPLETED);
	assert (pWrite->result() == 13);
	assert (pWrite->exception() == 0);
	assert (_completed == 2);
	
	reset();
	
	std::istringstream istr;
	AsyncStreamChannel ichannel(istr);
	pWrite = new AsyncWriteCommand("Hello, world!", 13);
	pWrite->commandFailed += delegate(this, &AsyncChannelTest::onFailed);
	ichannel.commandFailed += delegate(this, &AsyncChannelTest::onFailed);
	pWrite->commandCompleted += delegate(this, &AsyncChannelTest::onCompleted);
	ichannel.commandCompleted += delegate(this, &AsyncChannelTest::onCompleted);
	assert (pWrite->state() == AsyncCommand::CMD_PENDING);
	result = ichannel.enqueue(pWrite);
	pWrite->wait();
	result.wait();
	assert (!pWrite->succeeded());
	assert (pWrite->failed());
	assert (pWrite->exception() != 0);
	assert (_completed == 0);
	assert (_failed == 2);
}


void AsyncChannelTest::setUp()
{
	reset();
}


void AsyncChannelTest::tearDown()
{
}


void AsyncChannelTest::reset()
{
	_completed = 0;
	_failed    = 0;
}


void AsyncChannelTest::onCompleted(const void* sender, Poco::DeviceIO::AsyncEvent& event)
{
	++_completed;
}


void AsyncChannelTest::onFailed(const void* sender, Poco::DeviceIO::AsyncEvent& event)
{
	++_failed;
}


CppUnit::Test* AsyncChannelTest::suite()
{
	CppUnit::TestSuite* pSuite = new CppUnit::TestSuite("AsyncChannelTest");

	CppUnit_addTest(pSuite, AsyncChannelTest, testWrite);
	CppUnit_addTest(pSuite, AsyncChannelTest, testRead);
	CppUnit_addTest(pSuite, AsyncChannelTest, testSeek);
	CppUnit_addTest(pSuite, AsyncChannelTest, testEvents);

	return pSuite;
}
